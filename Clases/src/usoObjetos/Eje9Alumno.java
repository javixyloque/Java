package usoObjetos;

public class Eje9Alumno {
	private String nombre;
	private int edad;
	
	public void crearAlumno (String nombre, int edad) {
		this.nombre = nombre;
		this.edad = edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public int getEdad() {
		return this.edad;
	}
	
	
}
